//! Per-area chiptune patterns (plain data). Engine sequences via app adapter.

#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash)]
pub enum TrackId {
    Title,
    Village,
    Overworld,
    Dungeon,
    Boss,
    Victory,
}

#[derive(Clone, Copy, Debug)]
pub struct Note {
    pub start: f32,
    pub len: f32,
    pub midi: u8,
    pub vol: f32,
}

#[derive(Clone, Copy, Debug)]
pub struct MusicTrack {
    pub bpm: f32,
    pub loop_len: f32,
    pub channels: [&'static [Note]; 4],
}

const fn n(start: f32, len: f32, midi: u8, vol: f32) -> Note {
    Note {
        start,
        len,
        midi,
        vol,
    }
}

pub fn track(id: TrackId) -> MusicTrack {
    match id {
        TrackId::Title => TITLE,
        TrackId::Village => VILLAGE,
        TrackId::Overworld => OVERWORLD,
        TrackId::Dungeon => DUNGEON,
        TrackId::Boss => BOSS,
        TrackId::Victory => VICTORY,
    }
}

